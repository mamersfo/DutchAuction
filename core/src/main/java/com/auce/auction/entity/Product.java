
package com.auce.auction.entity;

import com.auce.util.Identity;

public class Product extends Entity implements Identity
{
	protected String	id;
	protected String	name;
	protected String	color;

	public Product( String id, String name, String color )
	{
		this.id = id;
		this.name = name;
		this.color = color;
	}

	public String getColor ()
	{
		return this.color;
	}

	public String getId ()
	{
		return id;
	}

	public String getName ()
	{
		return name;
	}
}
